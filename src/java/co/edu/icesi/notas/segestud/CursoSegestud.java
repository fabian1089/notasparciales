package co.edu.icesi.notas.segestud;

public class CursoSegestud {
	private String curso;

	private String matcod;

	private String matcon;

	private String materia;

	private String grupo;

	private String definitiva;

	public CursoSegestud() {
	}

	public String getCurso() {
		return curso;
	}

	public void setCurso(String curso) {
		this.curso = curso;
	}

	public String getGrupo() {
		return grupo;
	}

	public void setGrupo(String grupo) {
		this.grupo = grupo;
	}

	public String getMatcod() {
		return matcod;
	}

	public void setMatcod(String matcod) {
		this.matcod = matcod;
	}

	public String getMatcon() {
		return matcon;
	}

	public void setMatcon(String matcon) {
		this.matcon = matcon;
	}

	public String getMateria() {
		return materia;
	}

	public void setMateria(String materia) {
		this.materia = materia;
	}

	public String getDefinitiva() {
		return definitiva;
	}

	public void setDefinitiva(String definitiva) {
		this.definitiva = definitiva;
	}

}
